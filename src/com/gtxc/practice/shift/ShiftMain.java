public class ShiftMain {

    public static void main(String[] args) {
        // logic shift
        System.out.println(-2 >>> 1);
        System.out.println(Integer.MAX_VALUE);
        // arithmetic shift
        System.out.println(-2 >> 1); // -2/2
    }
}
